public class Leetcode_136 {
    /**
     * 数组
     *  任何数字和0 异或都等于 本身  任何数字异或本身 都等于0
     * @param nums
     * @return
     */
    public int singleNumber(int[] nums) {
        int single = 0;
        for (int num : nums) {
            single ^= num;
        }
        return single;
    }
}
